    private void showData() throws ParseException{
        hoTen.setText(user.getHoTen());
        userField.setText(user.getUsername());
        passField.setText(user.getPassword());
        SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
        dateField.setDate(sdf.parse(user.getNgaySinh()));
        gioiTinhField.setSelectedItem(user.getGioTinh());
        sdtField.setText(user.getDienThoai());
        emailField.setText(user.getEmail());
        quyenHanField.setSelectedItem(user.getQuyenHan());
        trangThaiLabel.setText("Trạng thái: " + user.getStatus());
    }
    private void updateActionPerformed(java.awt.event.ActionEvent evt) {                                       
        String username = userField.getText();
        String password = passField.getText();
        String hoTen = user.getHoTen();
        Date date = dateField.getDate();
        SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
        String ngaySinh = sdf.format(date);
        String gioiTinh = gioiTinhField.getSelectedItem().toString();
        String dienThoai = sdtField.getText();
        String email = emailField.getText();
        String quyenHan = quyenHanField.getSelectedItem().toString();
        NguoiDung user = new NguoiDung(username, password, hoTen, ngaySinh, gioiTinh, dienThoai, email, quyenHan, "Đang Hoạt Động");
        if(UserDAO.updateUser(user)){
            JOptionPane.showMessageDialog(this, "Cập nhật tài khoản thành công!", "Thông báo", JOptionPane.INFORMATION_MESSAGE);  
        }
        else{
            JOptionPane.showMessageDialog(this, "Cập nhật tài khoản thất bại!", "Thông báo", JOptionPane.INFORMATION_MESSAGE);  
        }
        return;
    }                                      

